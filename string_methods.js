//string method toUpperCase() and toLowerCase()
let text = "Hello World!";
let text1 = text.toUpperCase();
let text2 = text.toLowerCase();
console.log(text1);
console.log(text2);

//string method slice()
let text3 = "Hello World!";
let text4 = text3.slice(7, 12);
console.log(text4);
//First is the start index and second is the end index
//end index is not included

//string property length
let text5 = "Hello World!";
let text6 = text5.length;
console.log(text6);

//string method charAt()
let text7 = "Hello World!";
let text8 = text7.charAt(0);
console.log(text8);
